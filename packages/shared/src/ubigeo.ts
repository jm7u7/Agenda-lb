/** Normaliza para búsqueda tipográfica: sin tildes, minúsculas, espacios colapsados. */
export function normalizarBusqueda(s: string): string {
  return (s ?? '')
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .toLowerCase()
    .replace(/\s+/g, ' ')
    .trim();
}

