/** ¿`codigo` es un país válido del catálogo? (para la validación del backend, A4). */
export function esPaisValido(codigo: string): boolean {
  return PAISES.some((p) => p.codigo === codigo);
}

/** Nombre en español de un código ISO-2 (o el código si no está en el catálogo). */
export function nombrePais(codigo: string | null | undefined): string | null {
  if (!codigo) return null;
  return PAISES.find((p) => p.codigo === codigo)?.nombre ?? codigo;
}
